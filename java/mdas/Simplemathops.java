class Simplemathops {
    public static void main(String[] args) {
        int x, y, z;
        x=33;
        y=12;
        z=11;

        int xplusy = x + y;
        int xdivz = x / z;
        int yminusz = y - z;
        int zmulty = z * x;
        System.out.println(x +" + "+ y + " is: "+ xplusy); 
        System.out.println(x +" / "+ z + " is: "+ xdivz ); 
        System.out.println(y +" - "+ z + " is: "+ yminusz ); 
        System.out.println(z +" * "+ x + " is: "+ zmulty);  
    }
}